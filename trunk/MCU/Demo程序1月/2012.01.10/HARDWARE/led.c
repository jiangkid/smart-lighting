void Led_Init(void)
{
	RCC->APB2ENR|=1<<3;    //使能PORTB时钟	   	 
	   	 
	GPIOB->CRH&=0X0000FFFF; 
	GPIOB->CRH|=0X33330000;//PB12-15:LED0-LED3 推挽输出									  
}
